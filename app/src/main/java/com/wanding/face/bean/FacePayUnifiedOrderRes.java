package com.wanding.face.bean;

public class FacePayUnifiedOrderRes {

	/**
	 * 返回状态码
	 */
	private String return_code;

	/**
	 * 返回信息
	 */
	private String return_msg;

	/**
	 * 业务结果
	 */
	private String result_code;

	/**
	 * 错误代码
	 */
	private String err_code;

	/**
	 * 错误代码描述
	 */
	private String err_code_des;

	/**
	 * 商户号
	 */
	private String mch_no;

	/**
	 * 终端号
	 */
	private String term_no;

	/**
	 * 随机字符串
	 */
	private String nonce_str;

	/**
	 * 签名
	 */
	private String sign;

	/**
	 * 签名类型，目前支持MD5，默认为MD5
	 */
	private String sign_type;

	/**
	 * 交易单号
	 */
	private String trasaction_no;

	/**
	 * 订单金额(数字), 单位分
	 */
	private String total_fee;

	/**
	 * 交易完成时间，格式为yyyyMMddHHmmss
	 */
	private String trasaction_time;

	/**
	 * 交易类型
	 */
	private String pay_type;

	public String getReturn_code() {
		return return_code;
	}

	public void setReturn_code(String return_code) {
		this.return_code = return_code;
	}

	public String getReturn_msg() {
		return return_msg;
	}

	public void setReturn_msg(String return_msg) {
		this.return_msg = return_msg;
	}

	public String getResult_code() {
		return result_code;
	}

	public void setResult_code(String result_code) {
		this.result_code = result_code;
	}

	public String getErr_code() {
		return err_code;
	}

	public void setErr_code(String err_code) {
		this.err_code = err_code;
	}

	public String getErr_code_des() {
		return err_code_des;
	}

	public void setErr_code_des(String err_code_des) {
		this.err_code_des = err_code_des;
	}

	public String getMch_no() {
		return mch_no;
	}

	public void setMch_no(String mch_no) {
		this.mch_no = mch_no;
	}

	public String getTerm_no() {
		return term_no;
	}

	public void setTerm_no(String term_no) {
		this.term_no = term_no;
	}

	public String getNonce_str() {
		return nonce_str;
	}

	public void setNonce_str(String nonce_str) {
		this.nonce_str = nonce_str;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public String getSign_type() {
		return sign_type;
	}

	public void setSign_type(String sign_type) {
		this.sign_type = sign_type;
	}

	public String getTrasaction_no() {
		return trasaction_no;
	}

	public void setTrasaction_no(String trasaction_no) {
		this.trasaction_no = trasaction_no;
	}

	public String getTotal_fee() {
		return total_fee;
	}

	public void setTotal_fee(String total_fee) {
		this.total_fee = total_fee;
	}

	public String getTrasaction_time() {
		return trasaction_time;
	}

	public void setTrasaction_time(String trasaction_time) {
		this.trasaction_time = trasaction_time;
	}

	public String getPay_type() {
		return pay_type;
	}

	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}


}
